let answer = []
Page({
    /**
     * 生命周期函数--监听页面加载
     */
    onLoad() {
        wx.showLoading({
            title: 'Loading...',
        })
        let timer = setTimeout(() => {
            wx.hideLoading()
            clearTimeout(timer)
        }, 1000)
        this.setData({
            questionLen: question.length
        })
    },
    data: {
        question: question,
        index: 0,
        questionLen: 0,
        answer: [],
        result: false,
        start: true
    },
    radioChange(e) {
        answer[e.target.dataset.param] = e.detail.value
        console.log(answer)
    },
    next() {
        if (answer[this.data.index] == null) {
            // console.log("请选择")
            return;
        }
        this.setData({
            index: this.data.index + 1
        })
    },
    previous() {
        this.setData({
            index: this.data.index - 1
        })
    },
    redo() {
        answer = []
        this.setData({
            index: 0,
            result: false,
            answer: answer
        })
    },
    doSubmit() {
        if (answer[this.data.index] == null) {
            // console.log("请选择")
            return;
        }
        this.setData({
            answer: answer,
            result: true
        })
        // console.log("ok")
    }
})